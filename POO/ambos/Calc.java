public class Calc {
  public double somar(double n1, double n2){
    double res = n1+n2;
    return res;
  }
  public double subtrair(double n1, double n2){
    double res = n1-n2;
    return res;
  }
  public double multiplicar(double n1, double n2){
    double res = n1*n2;
    return res;
  }
  public double dividir(double n1, double n2){
    double res = n1/n2;
    return res;
  }
}
