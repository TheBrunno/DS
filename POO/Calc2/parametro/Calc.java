public class Calc {
  public void somar(int n1, int n2){
    System.out.println("seu resultado é: "+(n1+n2));
  }
  public void subtrair(int n1, int n2) {
    System.out.println("seu resultado é: "+(n1-n2));
  }
  public void multiplicar(int n1, int n2) {
    System.out.println("seu resultado é: "+(n1*n2));
  }
  public void dividir(int n1, int n2) {
    System.out.println("seu resultado é: "+(n1/n2));
  }
}
